#include<stdio.h>
int main()
{
	int a,b,c,d,e,f,g,h,i,j,k[18001];
	bool x[18001];
	while(scanf("%d%d%d",&a,&b,&c)==3)
	{
		if(a==0&&b==0&&c==0)
		{
			break;
		}
		for(d=0;d<18001;d++)
		{
			k[d]=0;
			x[d]=0;
		}
		for(d=0,e=a-5,f=a+5,h=0;d<18001;d+=f)
		{
			for(g=0;g<e&&d<18001;d++,g++)
			{
				if(h)
				{
					x[d]=1;
				}
				k[d]++;
			}
			h=1;
		}
		for(d=0,e=b-5,f=b+5,h=0;d<18001;d+=f)
		{
			for(g=0;g<e&&d<18001;d++,g++)
			{
				if(h)
				{
					x[d]=1;
				}
				k[d]++;
			}
			h=1;
		}
		if(c==0)
		{
			for(d=0;d<18001;d++)
			{
				if(k[d]==2)
				{
					if(x[d])
					{
						break;
					}
				}
			}
			if(d==18001)
			{
				printf("Signals fail to synchronise in 5 hours\n");
			}
			else
			{
				e=d/3600;
				f=d%3600;
				g=f%60;
				f/=60;
				printf("%02d:%02d:%02d\n",e,f,g);
			}
		}
		else
		{
			i=2;
			for(d=0,e=c-5,f=c+5,h=0;d<18001;d+=f)
			{
				for(g=0;g<e&&d<18001;d++,g++)
				{
					if(h)
					{
						x[d]=1;
					}
					k[d]++;
				}
				h=1;
			}
			i++;
			while(scanf("%d",&a)==1)
			{
				if(a==0)
				{
					break;
				}
				for(d=0,e=a-5,f=a+5,h=0;d<18001;d+=f)
				{
					for(g=0;g<e&&d<18001;d++,g++)
					{
						if(h)
						{
							x[d]=1;
						}
						k[d]++;
					}
					h=1;
				}
				i++;
			}
			for(d=0;d<18001;d++)
			{
				if(k[d]==i)
				{
					if(x[d])
					{
						break;
					}
				}
			}
			if(d==18001)
			{
				printf("Signals fail to synchronise in 5 hours\n");
			}
			else
			{
				e=d/3600;
				f=d%3600;
				g=f%60;
				f/=60;
				printf("%02d:%02d:%02d\n",e,f,g);
			}
		}
	}
	return 0;
}