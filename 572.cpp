#include<stdio.h>
char k[105][105];
int abc(int i,int j)
{
	int a[10050][2],x,y;
	x=1;
	y=0;
	while(y<x)
	{
		if(k[i+1][j]=='@')
		{
			k[i+1][j]='*';
			a[x][0]=i+1;
			a[x][1]=j;
			x++;
		}
		if(k[i+1][j-1]=='@')
		{
			k[i+1][j-1]='*';
			a[x][0]=i+1;
			a[x][1]=j-1;
			x++;
		}
		if(k[i+1][j+1]=='@')
		{
			k[i+1][j+1]='*';
			a[x][0]=i+1;
			a[x][1]=j+1;
			x++;
		}
		if(k[i][j-1]=='@')
		{
			k[i][j-1]='*';
			a[x][0]=i;
			a[x][1]=j-1;
			x++;
		}
		if(k[i][j]=='@')
		{
			k[i][j]='*';
			a[x][0]=i;
			a[x][1]=j;
			x++;
		}
		if(k[i][j+1]=='@')
		{
			k[i][j+1]='*';
			a[x][0]=i;
			a[x][1]=j+1;
			x++;
		}
		if(k[i-1][j-1]=='@')
		{
			k[i-1][j-1]='*';
			a[x][0]=i-1;
			a[x][1]=j-1;
			x++;
		}
		if(k[i-1][j]=='@')
		{
			k[i-1][j]='*';
			a[x][0]=i-1;
			a[x][1]=j;
			x++;
		}
		if(k[i-1][j+1]=='@')
		{
			k[i-1][j+1]='*';
			a[x][0]=i-1;
			a[x][1]=j+1;
			x++;
		}
		y++;
		i=a[y][0];
		j=a[y][1];
	}
	return 0;
}
int main()
{
	int a,b,c,d,e,f,g;
	while(scanf("%d %d\n",&a,&b)==2)
	{
		if(a==0&&b==0)
		{
			break;
		}
		g=0;
		for(c=0;c<a;c++)
		{
			gets(k[c]);
		}
		for(c=0;c<a;c++)
		{
			for(d=0;d<b;d++)
			{
				if(k[c][d]=='@')
				{
					abc(c,d);
					g++;
				}
			}
		}
		printf("%d\n",g);
	}
	return 0;
}