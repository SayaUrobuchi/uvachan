#include<stdio.h>
int main()
{
	int a,b,c,d,g,h,k[18];
	long long e,f,i,j;
	h=0;
	while(scanf("%d",&a)==1)
	{
		for(b=0,c=0,d=0,i=0,j=1;b<a;b++)
		{
			scanf("%d",&k[c]);
			if(k[c]==0)
			{
				if(c==0)
				{
					continue;
				}
				if(d%2==0)
				{
					if(j>i)
					{
						i=j;
					}
				}
				else if(c!=1||d!=c)
				{
					for(d=0,e=j;d<c;d++)
					{
						e/=k[d];
						if(e>0)
						{
							break;
						}
					}
					for(d=c-1,f=j;d>-1;d--)
					{
						f/=k[d];
						if(f>0)
						{
							break;
						}
					}
					if(e>f)
					{
						if(e>i)
						{
							i=e;
						}
					}
					else
					{
						if(f>i)
						{
							i=f;
						}
					}
				}
				j=1;
				c=0;
				d=0;
			}
			else
			{
				j*=k[c];
				if(k[c]<0)
				{
					d++;
				}
				c++;
			}
		}
		if(c!=0)
		{
		if(d%2==0)
		{
			if(j>i)
			{
				i=j;
			}
		}
		else if(c!=1||d!=c)
		{
			for(d=0,e=j;d<c;d++)
			{
				e/=k[d];
				if(e>0)
				{
					break;
				}
			}
			for(d=c-1,f=j;d>-1;d--)
			{
				f/=k[d];
				if(f>0)
				{
					break;
				}
			}
			if(e>f)
			{
				if(e>i)
				{
					i=e;
				}
			}
			else
			{
				if(f>i)
				{
					i=f;
				}
			}
		}
		}
		printf("Case #%d: The maximum product is %lld.\n\n",++h,i);
	}
	return 0;
}