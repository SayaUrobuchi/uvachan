#include<stdio.h>
int a,b,c,d,e,f,g,h,i,j,k[2500][15],m[2500][2],n[2501];
bool l[2500];
int main()
{
	while(scanf("%d",&a)==1)
	{
		for(b=0;b<a;b++)
		{
			scanf("%d",&k[b][0]);
			g=++k[b][0];
			for(c=1;c<g;c++)
			{
				scanf("%d",&k[b][c]);
			}
		}
		scanf("%d",&g);
		for(g;g>0;g--)
		{
			scanf("%d",&c);
			if(k[c][0]==1)
			{
				printf("0\n");
				continue;
			}
			for(b=0;b<a;b++)
			{
				l[b]=1;
				n[b]=0;
			}
			m[0][0]=c;
			m[0][1]=0;
			l[c]=0;
			for(b=0,c=1;b<c;b++)
			{
				i=m[b][0];
				j=m[b][1];
				for(d=1;d<k[i][0];d++)
				{
					if(l[k[i][d]])
					{
						n[j]++;
						m[c][0]=k[i][d];
						m[c++][1]=j+1;
						l[k[i][d]]=0;
					}
				}
			}
			for(b=0,i=0,j++;b<j;b++)
			{
				if(n[b]>i)
				{
					i=n[b];
					c=b;
				}
			}
			printf("%d %d\n",i,c+1);
		}
	}
}