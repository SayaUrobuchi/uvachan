#include<stdio.h>
#include<math.h>
int main()
{
	int a,b,c,f,g,h[12];
	double d,e,i,j,k[12][7],p,q,r,s;
	char x;
	a=1;
	while(scanf("%c",&x)==1)
	{
		if(x=='*')
		{
			break;
		}
		if(x=='r')
		{
			h[a]=1;
			scanf("%lf%lf%lf%lf%c",&k[a][0],&k[a][1],&k[a][2],&k[a][3],&x);
			a++;
		}
		else if(x=='c')
		{
			h[a]=2;
			scanf("%lf%lf%lf%c",&k[a][0],&k[a][1],&k[a][2],&x);
			k[a][2]*=k[a][2];
			a++;
		}
		else
		{
			h[a]=3;
			scanf("%lf%lf%lf%lf%lf%lf%c",&k[a][0],&k[a][1],&k[a][2],&k[a][3],&k[a][4],&k[a][5],&x);
			i=k[a][0]-k[a][2];
			i*=i;
			j=i;
			i=k[a][1]-k[a][3];
			i*=i;
			j+=i;
			j=sqrt(j);
			d=j;
			i=k[a][2]-k[a][4];
			i*=i;
			j=i;
			i=k[a][3]-k[a][5];
			i*=i;
			j+=i;
			j=sqrt(j);
			e=j;
			i=k[a][4]-k[a][0];
			i*=i;
			j=i;
			i=k[a][5]-k[a][1];
			i*=i;
			j+=i;
			j=sqrt(j);
			i=j;
			j+=d+e;
			j/=2;
			k[a][6]=sqrt(j*(j-d)*(j-e)*(j-i));
			//printf("%lf\n",k[a][6]);
			a++;
		}
	}
	g=0;
	while(scanf("%lf%lf",&i,&j)==2)
	{
		if(i==9999.9&&j==9999.9)
		{
			break;
		}
		g++;
		f=0;
		for(b=1;b<a;b++)
		{
			if(h[b]==1)
			{
				if(i<k[b][2]&&i>k[b][0])
				{
					if(j>k[b][3]&&j<k[b][1])
					{
						printf("Point %d is contained in figure %d\n",g,b);
						f++;
					}
				}
				continue;
			}
			if(h[b]==2)
			{
				d=i-k[b][0];
				d*=d;
				e=d;
				d=j-k[b][1];
				d*=d;
				e+=d;
				if(e<k[b][2])
				{
					printf("Point %d is contained in figure %d\n",g,b);
					f++;
				}
				continue;
			}
			d=i-k[b][0];
			d*=d;
			e=d;
			d=j-k[b][1];
			d*=d;
			e+=d;
			e=sqrt(e);
			p=e;
			//-------
			d=i-k[b][2];
			d*=d;
			e=d;
			d=j-k[b][3];
			d*=d;
			e+=d;
			e=sqrt(e);
			q=e;
			//-------
			d=i-k[b][4];
			d*=d;
			e=d;
			d=j-k[b][5];
			d*=d;
			e+=d;
			e=sqrt(e);
			r=e;
			//--------
			d=k[b][0]-k[b][2];
			d*=d;
			e=d;
			d=k[b][1]-k[b][3];
			d*=d;
			e+=d;
			e=sqrt(e);
			d=e;
			//-------
			d+=p+q;
			d/=2;
			e=sqrt(d*(d-p)*(d-q)*(d-e));
			if(e<=1E-6)
			{
				continue;
			}
			s=e;
			//-------
			d=k[b][2]-k[b][4];
			d*=d;
			e=d;
			d=k[b][3]-k[b][5];
			d*=d;
			e+=d;
			e=sqrt(e);
			d=e;
			//-------
			d+=q+r;
			d/=2;
			e=sqrt(d*(d-q)*(d-r)*(d-e));
			if(e<=1E-6)
			{
				continue;
			}
			s+=e;
			//--------
			d=k[b][4]-k[b][0];
			d*=d;
			e=d;
			d=k[b][5]-k[b][1];
			d*=d;
			e+=d;
			e=sqrt(e);
			d=e;
			//-------
			d+=p+r;
			d/=2;
			e=sqrt(d*(d-p)*(d-r)*(d-e));
			if(e<=1E-6)
			{
				continue;
			}
			s+=e;
			//--------
			if(s-k[b][6]<1E-6&&k[b][6]-s<1E-6)
			{
				printf("Point %d is contained in figure %d\n",g,b);
				f++;
			}
		}
		if(f==0)
		{
			printf("Point %d is not contained in any figure\n",g);
		}
	}
	return 0;
}