int abcc(const void *p,const void *q)
{
	if((*(abc*)p).y==(*(abc*)q).y)
	{
		return strcmp((*(abc*)p).z,(*(abc*)q).z);
	}
	return (*(abc*)q).y-(*(abc*)p).y;
}
