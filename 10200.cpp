#include<stdio.h>
#include<math.h>
int a,b,c,d,e,f,g,h,l[1230];
double i,j,m;
char k[10001];
int main()
{
	for(a=0;a<40;a++)
	{
		k[a]=1;
	}
	k[a]=0;
	l[0]=2;
	l[1]=3;
	for(a=5,b=2;a<10008;a+=2)
	{
		c=sqrt(a)+1;
		for(d=1;l[d]<c;d++)
		{
			if(!(a%l[d]))
			{
				break;
			}
		}
		if(l[d]>c-1)
		{
			l[b++]=a;
		}
	}
	for(a=41,b=1763;a<10001;a++,b+=a*2)
	{
		c=sqrt(b)+1;
		for(d=1;l[d]<c;d++)
		{
			if(!(b%l[d]))
			{
				break;
			}
		}
		if(l[d]<c)
		{
			k[a]=0;
		}
		else
		{
			k[a]=1;
		}
	}
	while(scanf("%d%d",&a,&b)==2)
	{
		for(c=a,d=b+1,i=0;c<d;c++)
		{
			i+=k[c];
		}
		if(!i)
		{
			printf("%.2f\n",0);
			continue;
		}
		i*=100;
		i/=d-a;
		j=i*100;
		j=(int)j;
		j/=100;
		m=j;
		j=i-j;
		i=m;
		if(j<0.005)
		{
			printf("%.2f\n",i);
		}
		else
		{
			printf("%.2f\n",i+0.01);
		}
	}
	return 0;
}