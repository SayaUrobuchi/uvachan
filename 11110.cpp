#include<stdio.h>
int a,b,c,d,e,f,g,h,i,j,k[100][100],l[10000][2],m[101];
char n;
int main()
{
	while(scanf("%d",&a)==1)
	{
		if(!a)
		{
			break;
		}
		for(b=0;b<a;b++)
		{
			for(c=0;c<a;c++)
			{
				k[b][c]=a;
			}
			m[b]=0;
		}
		for(b=1,c=a*a;b<a;b++)
		{
			while(scanf("%d%d%c",&d,&e,&n)==3)
			{
				c--;
				m[b]++;
				k[d-1][e-1]=b;
				if(n==10)
				{
					break;
				}
			}
		}
		m[b]=c;
		for(b=0,d=a;d;b++)
		{
			for(c=0;c<a;c++)
			{
				if(k[b][c])
				{
					l[0][0]=b;
					l[0][1]=c;
					h=k[b][c];
					k[b][c]=0;
					for(e=0,f=1;e<f;e++)
					{
						i=l[e][0];
						j=l[e][1];
						if(i>0)
						{
							if(k[i-1][j]==h)
							{
								l[f][0]=i-1;
								l[f++][1]=j;
								k[i-1][j]=0;
							}
						}
						if(i<a-1)
						{
							if(k[i+1][j]==h)
							{
								l[f][0]=i+1;
								l[f++][1]=j;
								k[i+1][j]=0;
							}
						}
						if(j>0)
						{
							if(k[i][j-1]==h)
							{
								l[f][0]=i;
								l[f++][1]=j-1;
								k[i][j-1]=0;
							}
						}
						if(j<a-1)
						{
							if(k[i][j+1]==h)
							{
								l[f][0]=i;
								l[f++][1]=j+1;
								k[i][j+1]=0;
							}
						}
					}
					if(f!=m[h])
					{
						break;
					}
					d--;
				}
			}
			if(f!=m[h])
			{
				break;
			}
		}
		if(f!=a)
		{
			printf("wrong\n");
		}
		else
		{
			printf("good\n");
		}
	}
	return 0;
}