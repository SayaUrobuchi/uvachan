#include<stdio.h>
int a[1001][3001],c[3000],d,e,f,g,h,j;
int main()
{
	a[0][0]=1;
	a[0][3000]=1;
	j=0;
	c[0]=0;
	for(d=1;d<1001;d++)
	{
		a[d][3000]=a[d-1][3000];
		c[0]++;
		for(f=0;f<=j;f++)
		{
			if(c[f]>9)
			{
				if(f==j)
				{
					c[f+1]=c[f]/10;
					j++;
				}
				else
				{
					c[f+1]+=c[f]/10;
				}
				c[f]%=10;
			}
		}
		if(c[0]==0)
		{
			for(f=0;f<a[d][3000];f++)
			{
				a[d][f]=0;
			}
		}
		else
		{
			for(f=0;f<a[d][3000];f++)
			{
				a[d][f]=c[0]*a[d-1][f];
			}
		}
		for(f=1;f<=j;f++)
		{
			if(c[f]!=0)
			{
				for(g=f,h=0;h<a[d][3000];h++,g++)
				{
					a[d][g]+=c[f]*a[d-1][h];
				}
			}
		}
		if(g!=0)
		{
			a[d][3000]=g;
			if(a[d][g]>9)
			{
				a[d][3000]++;
			}
		}
		for(f=0;f<a[d][3000];f++)
		{
			if(a[d][f]>9)
			{
				if(f==a[d][3000]-1)
				{
					a[d][3000]++;
					a[d][f+1]=a[d][f]/10;
				}
				else
				{
					a[d][f+1]+=a[d][f]/10;
				}
				a[d][f]%=10;
			}
		}
		/*if(d>=500&&d<550)
		{
			for(f=a[d][3000]-1;f>=0;f--)
			{
				printf("%d",a[d][f]);
			}
			printf("\n");
		}*/
	}
	while(scanf("%d",&d)==1)
	{
		printf("%d!\n",d);
		for(e=a[d][3000]-1;e>=0;e--)
		{
			printf("%d",a[d][e]);
		}
		printf("\n");
	}
	return 0;
}