#include<stdio.h>
#include<string.h>
int main()
{
	int a,b,c,d,e,f,g[10000],h[10000],i,j;
	char k[10000][41],x[1010],y[41],z[10000][41];
	while(scanf("%d",&a)==1)
	{
		j=0;
		for(;;)
		{
			gets(x);
			i=strlen(x);
			if(i==0)
			{
				continue;
			}
			else if(i==9)
			{
				if(strcmp(x,"EndOfText")==0)
				{
					break;
				}
			}
			for(b=0,c=0;b<i;b++)
			{
				if(x[b]>64&&x[b]<91)
				{
					y[c]=x[b]+32;
					c++;
				}
				else if(x[b]>96&&x[b]<123)
				{
					y[c]=x[b];
					c++;
				}
				else
				{
					if(c>0)
					{
						y[c]='\0';
						for(d=0;d<j;d++)
						{
							if(h[d]==c)
							{
								if(strcmp(k[d],y)==0)
								{
									g[d]++;
									break;
								}
							}
						}
						if(d==j)
						{
							strcpy(k[j],y);
							g[j]=1;
							h[j]=c;
							j++;
						}
						c=0;
					}
				}
			}
			if(c>0)
			{
				y[c]='\0';
				for(d=0;d<j;d++)
				{
					if(h[d]==c)
					{
						if(strcmp(k[d],y)==0)
						{
							g[d]++;
							break;
						}
					}
				}
				if(d==j)
				{
					strcpy(k[j],y);
					g[j]=1;
					h[j]=c;
					j++;
				}
			}
		}
		for(b=0,c=0;b<j;b++)
		{
			if(g[b]==a)
			{
				strcpy(z[c],k[b]);
				h[c]=h[b];
				c++;
			}
		}
		c--;
		for(b=0;b<c;b++)
		{
			for(d=0,e=c-b;d<c;d++)
			{
				i=h[d];
				if(h[d+1]<i)
				{
					i=h[d+1];
				}
				for(f=0,j=0;f<i;f++)
				{
					if(z[d][f]>z[d+1][f])
					{
						strcpy(x,z[d]);
						strcpy(z[d],z[d+1]);
						strcpy(z[d+1],x);
						i=h[d];
						h[d]=h[d+1];
						h[d+1]=i;
						break;
					}
					else if(z[d][f]==z[d+1][f])
					{
						j=1;
					}
					else
					{
						break;
					}
				}
				if(f==i&&j==1)
				{
					if(h[d]>h[d+1])
					{
						strcpy(x,z[d]);
						strcpy(z[d],z[d+1]);
						strcpy(z[d+1],x);
						i=h[d];
						h[d]=h[d+1];
						h[d+1]=i;
					}
				}
			}
		}
		if(c==-1)
		{
			printf("There is no such word.\n");
		}
		else
		{
			for(b=0;b<=c;b++)
			{
				printf("%s\n",z[b]);
			}
		}
		printf("\n");
	}
	return 0;
}