#include<stdio.h>
int main()
{
	int a,b,c,d,e,f,g,h,i[1200],j[1200][21];
	bool k[1200];
	while(scanf("%d",&a)==1)
	{
		scanf("%d",&h);
		k[0]=1;
		for(b=1;b<1200;b++)
		{
			i[b]=0;
			j[b][0]=1;
			k[b]=0;
		}
		g=0;
		for(h;h>0;h--)
		{
			scanf("%d",&b);
			for(c=g;c>=0;c--)
			{
				if(k[c]==1)
				{
					if(c+b<=a)
					{
						d=b+c;
						if(d>g)
						{
							g=d;
						}
						if(k[d]==1)
						{
							e=j[c][0];
							for(f=0;f<e;f++)
							{
								if(j[d][f]>i[c])
								{
									break;
								}
							}
							if(f<e)
							{
								for(f=0;f<e;f++)
								{
									j[d][f]=j[c][f];
									if(j[d][f]>i[d])
									{
										i[d]=j[d][f];
									}
								}
								e=j[d][0];
								j[d][e]=b;
								j[d][0]++;
							}
						}
						else
						{
							k[d]=1;
							e=j[c][0];
							for(f=0;f<e;f++)
							{
								j[d][f]=j[c][f];
								if(j[d][f]>i[d])
								{
									i[d]=j[d][f];
								}
							}
							e=j[d][0];
							j[d][e]=b;
							j[d][0]++;
						}
					}
				}
			}
		}
		for(a=0;;g--)
		{
			if(k[g]==1)
			{
				a=j[g][0];
				for(b=1;b<a;b++)
				{
					printf("%d ",j[g][b]);
				}
				printf("sum:%d\n",g);
				break;
			}
		}
	}
	return 0;
}