#include<stdio.h>
#include<string.h>
int a,b,c,d,e,f,g,h,i,j[52][5000];
char k[100001],x,y[1001];
int main()
{
	scanf("%d%c",&h,&x);
	for(h;h>0;h--)
	{
		gets(k);
		a=strlen(k);
		for(b=0;b<52;b++)
		{
			j[b][0]=1;
		}
		for(b=0;b<a;b++)
		{
			if(k[b]>90)
			{
				c=k[b]-97;
				j[c][j[c][0]]=b+1;
				j[c][0]++;
			}
			else
			{
				c=k[b]=39;
				j[c][j[c][0]]=b+1;
				j[c][0]++;
			}
		}
		scanf("%d%c",&g,&x);
		for(g;g>0;g--)
		{
			gets(y);
			b=strlen(y);
			if(b>a)
			{
				printf("n\n");
				continue;
			}
			c=a-b+2;
			x=k[0];
			if(x>90)
			{
				x-=97;
			}
			else
			{
				x-=39;
			}
			for(d=0;d<j[x][0];d++)
			{
				if(j[x][d]<c)
				{
					for(e=j[x][d],f=1;f<b;e++,f++)
					{
						if(k[e]!=y[f])
						{
							break;
						}
					}
					if(f==b)
					{
						printf("y\n");
						break;
					}
				}
				else
				{
					d=j[x][0];
					break;
				}
			}
			if(d==j[x][0])
			{
				printf("n\n");
			}
		}
	}
	return 0;
}